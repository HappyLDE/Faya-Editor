#ifndef LDE_VARIABLES_H
#define LDE_VARIABLES_H

typedef void            LDEvoid;
typedef signed char     LDEbyte;	// 1-byte signed
typedef short           LDEshort;	// 2-byte signed
typedef int             LDEint;		// 4-byte signed
typedef unsigned char   LDEubyte;	// 1-byte unsigned
typedef unsigned short  LDEushort;	// 2-byte unsigned
typedef unsigned int    LDEuint;	// 4-byte unsigned
typedef float           LDEfloat;	// single precision float
typedef double          LDEdouble;	// double precision float

#endif // LDE_VARIABLES_H
